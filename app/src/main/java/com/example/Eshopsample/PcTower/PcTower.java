package com.example.Eshopsample.PcTower;

public class PcTower {

    private  int id;
    private int memoryGb;
    private double cpuFrequencyGhz;

    public int getMemoryGb() {
        return memoryGb;
    }

    public void setMemoryGb(int memoryGb) {
        this.memoryGb = memoryGb;
    }

    public double getCpuFrequencyGhz() {
        return cpuFrequencyGhz;
    }

    public void setCpuFrequencyGhz(double cpuFrequencyGhz) {
        this.cpuFrequencyGhz = cpuFrequencyGhz;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }
}
